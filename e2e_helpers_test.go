package fusion_test

import "encoding/json"

func jsonMarshal(v any) ([]byte, error) { return json.Marshal(v) }

func contains(s, substr string) bool {
	return len(s) >= len(substr) && (func() bool {
		for i := 0; i+len(substr) <= len(s); i++ {
			if s[i:i+len(substr)] == substr {
				return true
			}
		}
		return false
	})()
}
